#include "shell.h"
#include "libft/libft.h"

void	one_cmd_exec_pwd()
{
	char	buf[1000];
	
	getcwd(buf, 1000);
	write(1, buf, ft_strlen(buf));
	write(1, "\n", 1);
	g_status = 0;
}