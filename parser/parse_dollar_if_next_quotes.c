#include "shell.h"
#include "libft/libft.h"

char	*parse_dollar_if_next_quotes(char *exchange, char *runner)
{
	char	*join2;

	*runner = '\0';
	join2 = runner + 1;
	exchange = ft_strjoin(exchange, join2);
	*runner = '$';
	return (exchange);
}